package Hello;

import java.util.Scanner;

public class Factorial2 {

	public static void main(String[] args) {

		// program to calculate the factorial of upto a given no.
				Scanner sc= new Scanner(System.in);
				System.out.println("enter the n value");
				int n= sc.nextInt();//Reading the value from the Scanner
			int fact =1;
				for(int i=1;i<=n;i++) {
					System.out.println("The factorial for the given no. : "+i);//printing the i value
					fact=fact*i;//multiplying the factorial
					System.out.println(fact);//printing the fact value
					
					
					
						
					
							
				}

				
				
				
				
	}
}




					
				
				
				
				
	
	


	

